#[cfg(test)]
mod tests {
    use super::*;

    fn make_kernel(
        name: &str,
        pattern: SOAC,
        in_ty: FutharkType,
        out_ty: FutharkType,
    ) -> KernelConfig {
        KernelConfig {
            name: name.to_string(),
            pattern,
            input_type: in_ty,
            output_type: out_ty,
            description: String::new(),
            operator: None,
            bins: None,
        }
    }

    #[test]
    fn test_map_kernel_generation() {
        let kernel = make_kernel("blur", SOAC::Map, FutharkType::F32, FutharkType::F32);
        let code = generate_map_entry(&kernel);
        assert!(code.contains("entry blur"));
        assert!(code.contains("map (\\x -> x) xs"));
        assert!(code.contains("[]f32"));
    }

    #[test]
    fn test_reduce_kernel_generation() {
        let mut kernel = make_kernel("total", SOAC::Reduce, FutharkType::F64, FutharkType::F64);
        kernel.operator = Some("+".to_string());
        let code = generate_reduce_entry(&kernel);
        assert!(code.contains("entry total"));
        assert!(code.contains("reduce (+) 0.0 xs"));
        // Output type for reduce is scalar, not array.
        assert!(code.contains(": f64"));
    }

    #[test]
    fn test_scan_kernel_generation() {
        let mut kernel = make_kernel("prefix_sum", SOAC::Scan, FutharkType::F64, FutharkType::F64);
        kernel.operator = Some("+".to_string());
        let code = generate_scan_entry(&kernel);
        assert!(code.contains("entry prefix_sum"));
        assert!(code.contains("scan (+) 0.0 xs"));
        assert!(code.contains("[]f64"));
    }

    #[test]
    fn test_scatter_kernel_generation() {
        let kernel = make_kernel("write", SOAC::Scatter, FutharkType::F32, FutharkType::F32);
        let code = generate_scatter_entry(&kernel);
        assert!(code.contains("entry write"));
        assert!(code.contains("scatter dest is vs"));
        assert!(code.contains("*[]f32"));
    }

    #[test]
    fn test_histogram_kernel_generation() {
        let mut kernel = make_kernel("hist", SOAC::Histogram, FutharkType::U8, FutharkType::I64);
        kernel.bins = Some(256);
        let code = generate_histogram_entry(&kernel);
        assert!(code.contains("entry hist"));
        assert!(code.contains("reduce_by_index"));
        assert!(code.contains("replicate 256"));
        assert!(code.contains("map i64.u8 xs"));
    }

    #[test]
    fn test_full_source_generation() {
        let manifest = Manifest {
            project: crate::manifest::ProjectConfig {
                name: "test-project".to_string(),
            },
            kernels: vec![],
            gpu: crate::manifest::GpuConfig::default(),
        };
        let kernels = vec![make_kernel(
            "k1",
            SOAC::Map,
            FutharkType::F32,
            FutharkType::F32,
        )];
        let source = generate_futhark_source(&manifest, &kernels).expect("TODO: handle error");
        assert!(source.contains("Auto-generated by futharkiser"));
        assert!(source.contains("entry k1"));
        assert!(source.contains("PMPL-1.0-or-later"));
    }

    #[test]
    fn test_reduce_with_multiply_operator() {
        let mut kernel = make_kernel("product", SOAC::Reduce, FutharkType::I32, FutharkType::I32);
        kernel.operator = Some("*".to_string());
        let code = generate_reduce_entry(&kernel);
        assert!(code.contains("reduce (*) 1 xs"));
    }

    #[test]
    fn test_description_becomes_comment() {
        let mut kernel = make_kernel("blur", SOAC::Map, FutharkType::F32, FutharkType::F32);
        kernel.description = "Gaussian blur".to_string();
        let code = generate_kernel_entry(&kernel).expect("TODO: handle error");
        assert!(code.contains("-- | Gaussian blur"));
    }
}
